def get_public_ip():
    ip_services = [
        "https://api.ipify.org",
        "https://icanhazip.com",
        "https://ifconfig.me/ip",
    ]
    for url in ip_services:
        try:
            resp = requests.get(url, timeout=3)
            if resp.status_code == 200:
                ip = resp.text.strip()
                if ip and ip != "127.0.0.1":
                    return ip
        except requests.RequestException:
            continue
    return None


