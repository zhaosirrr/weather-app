def _bounded(v, lo=15, hi=98):
    return max(lo, min(hi, v))


def get_tarot_fortune(weather_condition, temp, date_str):
    rng = random.Random(date_str)

    card = rng.choice(TAROT_CARDS)
    orientation = "upright" if rng.random() < 0.65 else "reversed"
    detail = card[orientation]

    theme_key = "cloudy"
    if weather_condition:
        wc = weather_condition.lower()
        if "晴" in weather_condition or "sun" in wc or "clear" in wc:
            theme_key = "sunny"
        elif "雨" in weather_condition or "rain" in wc or "drizzle" in wc:
            theme_key = "rainy"
        elif "雪" in weather_condition or "snow" in wc:
            theme_key = "snowy"
        elif "雷" in weather_condition or "暴" in wc or "thunder" in wc:
            theme_key = "storm"
        elif "雾" in weather_condition or "霾" in wc or "fog" in wc or "mist" in wc:
            theme_key = "fog"

    base_by_theme = {
        "sunny": 78,
        "cloudy": 62,
        "rainy": 58,
        "snowy": 66,
        "storm": 50,
        "fog": 55,
    }
    base = base_by_theme.get(theme_key, 60)
    if orientation == "upright":
        luck_score = base + rng.randint(0, 15)
    else:
        luck_score = base - 15 + rng.randint(-5, 10)
    luck_score = max(25, min(95, luck_score))
    luck_stars = round((luck_score / 100) * 5 * 2) / 2
    luck_stars = max(0.5, min(5.0, luck_stars))

    dim_center = 68 if orientation == "upright" else 42
    dimension_scores = {
        "career": _bounded(dim_center + rng.randint(-14, 20)),
        "love":   _bounded(dim_center + rng.randint(-14, 20)),
        "social": _bounded(dim_center + rng.randint(-14, 20)),
        "wealth": _bounded(dim_center + rng.randint(-14, 20)),
        "health": _bounded(dim_center + rng.randint(-14, 20)),
    }

    dos = sorted(rng.sample(DAILY_DOS_POOL, 2), key=DAILY_DOS_POOL.index)
    donts = sorted(rng.sample(DAILY_DONTS_POOL, 2), key=DAILY_DONTS_POOL.index)

    return {
        "card": card["name"],
        "card_en": card["name_en"],
        "card_number": card["number"],
        "card_key": card["key"],
        "orientation": orientation,
        "orientation_label": "正位" if orientation == "upright" else "逆位",
        "summary": detail["summary"],
        "keywords": detail["keywords"],
        "luck_score": luck_score,
        "luck_stars": luck_stars,
        "theme": theme_key,
        "advice": {
            "career": detail["career"],
            "love": detail["love"],
            "social": detail["social"],
            "wealth": detail["wealth"],
            "health": detail["health"],
        },
        "dimension_scores": dimension_scores,
        "dos": dos,
        "donts": donts,
    }

